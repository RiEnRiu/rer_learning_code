def argsort_bubble(data):
    output = list(range(len(data)))
    t_data = data.copy()
    for i in range(len(t_data) - 1):
        for j in range(len(t_data) - i - 1):
            if t_data[j] > t_data[j + 1]:
                t_data[j], t_data[j + 1] = t_data[j + 1], t_data[j]
                output[j], output[j + 1] = output[j + 1], output[j]
    return output

